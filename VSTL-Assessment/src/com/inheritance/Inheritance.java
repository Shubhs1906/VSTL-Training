package com.inheritance;

public class Inheritance {

	public void doAddition(int intNumberA, int intNumberB) {

		System.out.println("Addition of Numbers - ");

		System.out.println("Number A : " + intNumberA);

		System.out.println("Number B : " + intNumberB);

		System.out.println("Addition is : " + (intNumberA + intNumberB));

	}

	public void doDivision(int intNumberA, int intNumberB) {

		System.out.println("********");

		System.out.println("Division of Numbers - ");

		System.out.println("Number A : " + intNumberA);

		System.out.println("Number B : " + intNumberB);

		System.out.println("Division is : " + (intNumberA / intNumberB));

	}

	public void doSubstraction(int intNumberA, int intNumberB) {

		System.out.println("********");

		System.out.println("Substraction of Numbers - ");

		System.out.println("Number A : " + intNumberA);

		System.out.println("Number B : " + intNumberB);

		System.out.println("Substraction is : " + (intNumberA - intNumberB));

	}

	public void doMultiplication(int intNumberA, int intNumberB) {

		System.out.println("********");

		System.out.println("Multiplication of Numbers - ");

		System.out.println("Number A : " + intNumberA);

		System.out.println("Number B : " + intNumberB);

		System.out.println("Multiplication is : " + (intNumberA * intNumberB));

	}

}
